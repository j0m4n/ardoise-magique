package com.fenetre;

public class Point {
	public int size;// = 20;
	public int posX;
	public int posY;
	public String forme;
	public String color;
	
	public Point(int size, int posX, int posY, String forme, String color)
	{
		this.size = size;
		this.posX = posX;
		this.posY = posY;
		this.forme = forme;
		this.color = color;
	}

}
